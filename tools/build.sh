#!/usr/bin/env bash
# tools/build.sh — S5-009 export pipeline for Railhunter.
#
# Builds release binaries for Linux/X11 and Windows using Godot 4.6's
# headless export. Outputs go to build/ in the project root.
#
# REQUIREMENTS (one-time setup per dev machine):
#   1. Godot 4.6.1+ installed and `godot` in PATH (or GODOT_BIN env var)
#   2. Export templates downloaded for Linux + Windows:
#        Linux:  godot --headless --quit --install-linux-export-template
#        Windows: godot --headless --quit --install-windows-export-template
#      (or via Godot editor: Editor > Manage Export Templates)
#   3. export_presets.cfg generated by opening the project in Godot
#      editor ONCE and using Project > Export > Add > preset. After
#      saving, commit the resulting export_presets.cfg. This file is
#      platform-binary and editor-version specific; re-generate if
#      upgrading Godot.
#
# USAGE:
#   tools/build.sh                # build both Linux + Windows
#   tools/build.sh linux          # build only Linux/X11
#   tools/build.sh windows        # build only Windows Desktop
#   tools/build.sh debug linux    # build debug (faster, not minified)
#
# OUTPUT:
#   build/railhunter.x86_64         (Linux)
#   build/railhunter.exe            (Windows)
#   build/railhunter-debug.x86_64   (Linux, debug build)
#
# EXIT CODES:
#   0  success
#   1  godot binary not found
#   2  export templates missing
#   3  export_presets.cfg missing (run editor once)
#   4  godot --export-* returned non-zero
#   5  invalid CLI argument

set -e

GODOT_BIN="${GODOT_BIN:-godot}"
PROJECT_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
cd "$PROJECT_ROOT"

# Parse args
TARGETS=()
RELEASE=1  # default: release build
for arg in "$@"; do
    case "$arg" in
        debug)     RELEASE=0 ;;
        linux|windows) TARGETS+=("$arg") ;;
        -h|--help)
            head -30 "$0"
            exit 0
            ;;
        *)
            echo "Unknown argument: $arg" >&2
            echo "Usage: $0 [debug] [linux] [windows]" >&2
            exit 5
            ;;
    esac
done
if [ ${#TARGETS[@]} -eq 0 ]; then
    TARGETS=(linux windows)
fi

# Sanity checks
if ! command -v "$GODOT_BIN" >/dev/null 2>&1; then
    echo "ERROR: godot binary '$GODOT_BIN' not found in PATH" >&2
    echo "  Set GODOT_BIN env var or install Godot 4.6+" >&2
    exit 1
fi

if [ ! -f "export_presets.cfg" ]; then
    echo "ERROR: export_presets.cfg not found" >&2
    echo "  Open project in Godot editor, run Project > Export > Add > preset" >&2
    echo "  for each target platform, then save. Commit the resulting" >&2
    echo "  export_presets.cfg to the repo." >&2
    exit 3
fi

mkdir -p build

# Build each target
build_one() {
    local target="$1"
    local preset_name
    local out_file
    if [ "$RELEASE" -eq 1 ]; then
        preset_name="Linux/X11"
        out_file="railhunter.x86_64"
        [ "$target" = "windows" ] && preset_name="Windows Desktop" && out_file="railhunter.exe"
    else
        preset_name="Linux/X11 (Debug)"
        out_file="railhunter-debug.x86_64"
        [ "$target" = "windows" ] && preset_name="Windows Desktop (Debug)" && out_file="railhunter-debug.exe"
    fi
    echo "==> Building $target (preset: '$preset_name') -> build/$out_file"
    if ! "$GODOT_BIN" --headless --path "$PROJECT_ROOT" --export-release "$preset_name" "build/$out_file"; then
        if [ "$RELEASE" -eq 0 ]; then
            "$GODOT_BIN" --headless --path "$PROJECT_ROOT" --export-debug "$preset_name" "build/$out_file"
        else
            echo "ERROR: godot --export-release failed for $target" >&2
            exit 4
        fi
    fi
}

for target in "${TARGETS[@]}"; do
    build_one "$target"
done

echo ""
echo "Build complete. Artifacts in build/:"
ls -lh build/ 2>/dev/null | grep -E "railhunter" || echo "  (none found)"
exit 0
