#!/usr/bin/env python3
"""
sync_input_bindings.py — S5-008 guard. Verifies project.godot's [input]
section doesn't have orphan action definitions (i.e., every action in
project.godot must be defined in the YAML source-of-truth).

Per ADR-0009, the YAML (design/registry/input-bindings.yaml) is the
canonical source for the 47-action input map. The runtime artifact
(project.godot [input] section) is supposed to be generated by a CI
sync step that doesn't exist yet (Sprint 5+ backlog). This lint catches
the *reverse* error: if a developer adds an action to project.godot but
forgets to update the YAML, this will detect it.

Note: This lint does NOT enforce that project.godot contains all 47
YAML actions — that requires a CI sync generator which is out of S5-008
scope. Once the generator exists, this lint can be extended to also
check for missing-from-project.godot actions.

Exits 0 if consistent, 1 if any project.godot action is missing from YAML.

Usage:
  python tools/sync_input_bindings.py
  python tools/sync_input_bindings.py --ci
"""
import os
import re
import sys

# Godot 4 default InputMap actions that exist in any Godot project
# without being declared in project.godot. These are NOT orphans;
# they're built-in defaults. If we add custom actions that share
# these names, project.godot overrides them but YAML must declare them.
GODOT_BUILTIN_ACTIONS: set[str] = {
    "ui_accept", "ui_cancel", "ui_left", "ui_right", "ui_up", "ui_down",
    "ui_page_up", "ui_page_down", "ui_home", "ui_end",
    "ui_text_completion_accept", "ui_text_completion_request",
    "ui_text_completion_option",
    "ui_focus_next", "ui_focus_prev",
    "ui_left_w", "ui_right_w", "ui_up_w", "ui_down_w",  # _w weird variants
    "ui_select", "ui_index_next", "ui_index_prev",
    "ui_hover_first", "ui_hover_previous",
    "ui_menu", "ui_text_indent", "ui_text_dedent",
    "ui_grab", "ui_release",
}


def parse_yaml_action_names(yaml_path: str) -> set[str]:
    """Extract top-level action names (lines matching '<name>:' at col 0)."""
    if not os.path.isfile(yaml_path):
        return set()
    names: set[str] = set()
    with open(yaml_path, "r", encoding="utf-8") as f:
        for line in f:
            m = re.match(r"^([a-z_][a-z_0-9]*):\s*$", line)
            if m:
                names.add(m.group(1))
    return names


def parse_project_godot_action_names(project_godot: str) -> set[str]:
    """Extract action names from project.godot [input] section."""
    if not os.path.isfile(project_godot):
        print(f"ERROR: {project_godot} not found")
        sys.exit(1)
    names: set[str] = set()
    in_input = False
    with open(project_godot, "r", encoding="utf-8") as f:
        for line in f:
            stripped = line.strip()
            if stripped == "[input]":
                in_input = True
                continue
            if in_input:
                if stripped.startswith("["):
                    break
                m = re.match(r'^([a-z_][a-z_0-9_]*)="?\{', stripped)
                if m:
                    names.add(m.group(1))
    return names


def lint(project_godot: str = "project.godot",
         yaml_path: str = "design/registry/input-bindings.yaml") -> list[str]:
    issues: list[str] = []
    yaml_actions = parse_yaml_action_names(yaml_path)
    project_actions = parse_project_godot_action_names(project_godot)
    if not yaml_actions:
        issues.append(f"no actions parsed from {yaml_path} (file missing or empty)")
    # Project.godot actions that are NOT in YAML (and not Godot builtins)
    orphans = project_actions - yaml_actions - GODOT_BUILTIN_ACTIONS
    for orphan in sorted(orphans):
        issues.append(
            f"project.godot action '{orphan}' is not declared in {yaml_path}"
        )
    return issues


def main() -> int:
    args = sys.argv[1:]
    ci_mode = "--ci" in args
    args = [a for a in args if a != "--ci"]
    project_godot = args[0] if args else "project.godot"
    yaml_path = "design/registry/input-bindings.yaml"
    if len(args) > 1:
        yaml_path = args[1]
    issues = lint(project_godot, yaml_path)
    if issues:
        print("=== Input Binding Sync Lint FAILED ===")
        print("Per ADR-0009, project.godot actions must be declared in the YAML source-of-truth.")
        print("\nIssues:")
        for issue in issues:
            print(f"  - {issue}")
        print(
            f"\nEither add the action to {yaml_path} or remove from project.godot."
        )
        return 1
    else:
        project_count = len(parse_project_godot_action_names(project_godot))
        yaml_count = len(parse_yaml_action_names(yaml_path))
        print("=== Input Binding Sync Lint OK ===")
        print(f"project.godot: {project_count} actions | YAML: {yaml_count} actions")
        print("No orphan actions. (Note: project.godot may be a subset of YAML")
        print("— full sync-to-47 generator is post-Sprint 5 backlog.)")
        return 0


if __name__ == "__main__":
    sys.exit(main())
